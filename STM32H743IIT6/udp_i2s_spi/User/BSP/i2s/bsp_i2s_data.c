/*24位转16位存储数组*/
void Convert24To16(uint32_t* data_array,int length)
{
    int i = 0;
    for(i = 0;i<length;i=i+4)//默认左声道
    {
       data_array[i+1] = (data_array[i]>>8) & 0xffff;
       data_array[i] = (data_array[i] << 8) & 0xffff;
       data_array[i+2] = 0x00;
	   data_array[i+3] = 0x00;
    }
}

