/*24位转16位存储数组*/
void Convert24To16(int16_t* data_array_out,int32_t* data_array,uint8_t channel,int length)
{
    int i = 0;
    int index = 0;
    if(channel & 0x02)
    {
        for(i = 0;i<length;i++)//默认右声道
        {
            data_array_out[index+5] = (data_array[i]>>16) & 0x00ff;
            data_array_out[index+4] = (data_array[i]) & 0xffff;
            data_array_out[index+6] = 0x00;
            data_array_out[index+7] = 0x00;

            data_array_out[index]   = 0x0000;
            data_array_out[index+1] = 0x0000;
            data_array_out[index+3] = 0x0000;
            data_array_out[index+2] = 0x0000;
            index+=8;
            
        }        
    }
    else if(channel & 0x01)
    {
        for(i = 0;i<length;i++)//默认左声道
        {
           data_array_out[index+1]    = (data_array[i]>>16) & 0x00ff;
           data_array_out[index]      = (data_array[i]) & 0xffff;
           data_array_out[index+2]  = 0x00;
           data_array_out[index+3]  = 0x00;
            


            data_array_out[index+5] = 0x00;
            data_array_out[index+4] = 0x00;
            data_array_out[index+6] = 0x00;
            data_array_out[index+7] = 0x00;
            index += 8;
        }
    }
    
}

